# addition...
def sum(a, b):
    c = a + b
    return c 

# multiplication....
def mul(m, n):
    res = m * n
    return res

# substraction....
def sub(a, b):
    c = a - b
    return c

# division....
def div(a, b):
    c = a / b
    return c
    


diff = sub(10, 5)
print(f"sub of two no is: {diff}")

product = mul(3, 3)
print(f"multiplication of m and n is : {product}" )

add = sum(40, 50)
print(f"sum of the number: {add}") 

division = div(50, 5)
print(f"div of two no is: {division}")

